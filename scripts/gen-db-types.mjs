#!/usr/bin/env node
// Regenerate src/types/database.generated.ts from the linked Supabase
// project. This script is a thin wrapper around
//   supabase gen types typescript --linked --schema public
// so that:
//   - The output path is consistent (src/types/database.generated.ts)
//   - The output is prefixed with a do-not-edit banner
//   - Stderr from the CLI surfaces clearly without being swallowed
//
// Prerequisites:
//   - npx supabase login
//   - npx supabase link --project-ref gndnldyfudbytbboxesk
//
// Both prerequisites are founder actions documented in
// docs/hardening/phase1/db-types-automation.md.

import { execFileSync } from 'node:child_process'
import { mkdirSync, writeFileSync } from 'node:fs'
import { dirname, resolve } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const repoRoot = resolve(__dirname, '..')
const outPath = resolve(repoRoot, 'src/types/database.generated.ts')

const banner = `// Auto-generated by scripts/gen-db-types.mjs from the linked Supabase
// project. Do NOT edit by hand. Re-run via:
//   npm run db:types
//
// The hand-written src/types/database.ts is the codebase's public type
// surface and re-exports a curated subset of these generated types.
// See docs/hardening/phase1/db-types-automation.md.

`

function run() {
  let stdout
  try {
    stdout = execFileSync(
      'npx',
      ['supabase', 'gen', 'types', 'typescript', '--linked', '--schema', 'public'],
      { encoding: 'utf8', stdio: ['ignore', 'pipe', 'inherit'], shell: process.platform === 'win32' }
    )
  } catch (err) {
    console.error('\n[gen-db-types] supabase gen types FAILED.')
    console.error('[gen-db-types] Most common cause: project not linked.')
    console.error('[gen-db-types] Run:')
    console.error('  npx supabase login')
    console.error('  npx supabase link --project-ref gndnldyfudbytbboxesk')
    process.exit(typeof err.status === 'number' ? err.status : 1)
  }

  mkdirSync(dirname(outPath), { recursive: true })
  writeFileSync(outPath, banner + stdout, 'utf8')
  console.log(`[gen-db-types] wrote ${outPath} (${stdout.length} chars)`)
}

run()
